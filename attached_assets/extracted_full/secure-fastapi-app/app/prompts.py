LANG_NAMES = {
    "ar": "العربية", "en": "English", "fr": "Français", "es": "Español",
    "de": "Deutsch", "tr": "Türkçe", "ur": "اردو", "hi": "हिन्दी",
}


def lang_note(lang: str, has_image: bool = False) -> str:
    """يبني تعليمة اللغة: auto = بنفس لغة الاستعلام؛ وللصور بلا نص = الإنجليزية."""
    lang = (lang or "auto").strip().lower()
    if lang == "auto":
        if has_image:
            return "اكتب جميع الحقول النصية باللغة الإنجليزية (English)."
        return ("اكتشف لغة استعلام المستخدم واكتب جميع الحقول النصية بنفس تلك اللغة، "
                "أياً كانت (العربية، الإنجليزية، الفرنسية، الإسبانية، التركية، ...).")
    name = LANG_NAMES.get(lang, lang)
    return f"اكتب جميع الحقول النصية باللغة التالية حصراً: {name} (رمزها: {lang})."
